package heap;

public class Person {
	private int t1;
	private int t2;
	private int t3;
	
	
	
	public Person(int t1, int t2, int t3) {
		super();
		this.t1 = t1;
		this.t2 = t2;
		this.t3 = t3;
	}

	public int getT1() {
		return t1;
	}


	public void setT1(int t1) {
		this.t1 = t1;
	}


	public int getT2() {
		return t2;
	}


	public void setT2(int t2) {
		this.t2 = t2;
	}


	public int getT3() {
		return t3;
	}


	public void setT3(int t3) {
		this.t3 = t3;
	}
}
